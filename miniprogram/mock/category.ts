export function normalizeRootCategoryId(categoryId?: string) {
  if (rootCategories.some((item) => item.id === categoryId)) {
    return categoryId ?? rootCategories[0].id;
  }

  return rootCategories[0].id;
}

export function getCategoryPageData(selectedCategoryId: string, favoriteIds: string[]) {
  const currentCategory =
    rootCategories.find((item) => item.id === normalizeRootCategoryId(selectedCategoryId)) ?? rootCategories[0];

  const categoryProducts = searchProducts({
    keyword: "",
    categoryId: currentCategory.id,
    sortOption: "sales_desc",
    filterState: defaultSearchFilterState,
    favoriteIds,
  }).slice(0, 3);

  return {
    rootCategories,
    currentCategory,
    subCategories: currentCategory.children,
    categoryProducts: categoryProducts as SearchProduct[],
    selectedCategoryId: currentCategory.id,
  };
}
